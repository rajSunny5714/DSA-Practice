// Identify keywords, identifiers, literals, and tokens in a program.

#include<iostream>
using namespace std;
int main(){
    int a=10;
    float b=5.5;
    char c='X';
    cout<<"Keywords: int float char return"<<endl;
    cout<<"Identifiers: main a b c"<<endl;
    cout<<"Literals: 10 5.5 'X'"<<endl;
    cout<<"Tokens: keywords identifiers literals operators separators"<<endl;
    return 0;
}
